<h2>Levels</h2> <br>
1. Trainee: <br>
As a trainee you can start executing the game of the day (see above). <p><br></p>

2. Apprentice: <br>
You have trainee skills plus you can vote on Game of the Day Proposals.<p><br></p>

3. Master of the Game: <br>
You have apprentice skills plus you can submit your own Game of the Day Proposals.

